#include <stdio.h>

int main() {
    // Declare variables with different data types
    int age = 25;
    float weight = 68.5;
    char gender = 'M';
    char name[] = "John";


    // Display the values of variables using printf function
    printf("Age: %d\n", age);
    printf("Weight: %.1f\n", weight);
    printf("Gender: %c\n", gender);
    printf("Name: %s\n", name);

    return 0;
}
